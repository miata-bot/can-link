defmodule CANLink.RGB do
  use GenServer
  require Logger

  def set_color(r, g, b) do
    GenServer.cast(__MODULE__, {:set_color, r, g, b})
  end

  def set_rpm(rpm) do
    idk = rpm * 5 / 38
    row = Enum.at(map(), round(idk))

    if(row) do
      r = elem(row, 0)
      g = elem(row, 1)
      b = elem(row, 2)
      set_color(r, g, b)
    end
  end

  @doc false
  def start_link(args) do
    GenServer.start_link(__MODULE__, args, name: __MODULE__)
  end

  def init(args) do
    tty = Keyword.fetch!(args, :tty)

    opts = [
      speed: 115_200,
      framing: Circuits.UART.Framing.None
    ]

    send(self(), :open_uart)
    {:ok, uart} = Circuits.UART.start_link()
    {:ok, %{uart: uart, tty: tty, opts: opts, opened?: false, r: args[:r] || 0, g: args[:g] || 0, b: args[:b] || 0}}
  end

  def handle_info(:open_uart, state) do
    case Circuits.UART.open(state.uart, state.tty, state.opts) do
      :ok ->
        Logger.info("Opened uart: #{inspect(state.tty)}")
        Circuits.UART.write(state.uart, <<"r", state.r::8, "g", state.g::8, "b", state.b::8>>)

        {:noreply, %{state | opened?: true}}

      error ->
        Logger.error("Failed to open uart: #{inspect(error)}")
        {:stop, error, state}
    end
  end

  def handle_cast({:set_color, r, g, b}, state) do
    if state.opened? do
      Circuits.UART.write(state.uart, <<"r", r::8, "g", g::8, "b", b::8>>)
    end

    {:noreply, %{state | r: r, g: g, b: b}}
  end

